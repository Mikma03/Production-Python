# Given an integer array nums, return true if any value appears at least twice in the array, and return false if every element is distinct.

myArr = [1,2,3,4, 1]

class Solution:
    @staticmethod
    def checker(array):
        set_ = set()

        for i in array:
            if i in set_:
                return True
            set_.add(i)
        
        if len(array) == len(set_):
            return False


